	package vo;

public class Point {
	private int pointNo;
	private int orderNo;
	private String pointPm;
	private int point;
	private String createdate;
	private String productName;
	private String productSaveFileName;
	
	public int getPointNo() {
		return pointNo;
	}
	public void setPointNo(int pointNo) {
		this.pointNo = pointNo;
	}
	public int getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(int orderNo) {
		this.orderNo = orderNo;
	}
	public String getPointPm() {
		return pointPm;
	}
	public void setPointPm(String pointPm) {
		this.pointPm = pointPm;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	public String getCreatedate() {
		return createdate;
	}
	public void setCreatedate(String createdate) {
		this.createdate = createdate;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductSaveFileName() {
		return productSaveFileName;
	}
	public void setProductSaveFileName(String productSaveFileName) {
		this.productSaveFileName = productSaveFileName;
	}
}
